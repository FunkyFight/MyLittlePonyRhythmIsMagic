    public static void Draw(SpriteBatch spriteBatch, Texture2D pixel, string text, Vector2 position, Color color, int scale)
    {
        if (string.IsNullOrEmpty(text))
            return;

        int x = 0;
        int y = 0;

        foreach (char rawChar in text)
        {
            if (rawChar == '\n')
            {
                x = 0;
                y += (Height + 2) * scale;
                continue;
            }

            char c = char.ToUpperInvariant(rawChar);
            if (!Glyphs.TryGetValue(c, out string[] glyph))
                glyph = Glyphs['?'];

            for (int row = 0; row < Height; row++)
            {
                for (int col = 0; col < Width; col++)
                {
                    if (glyph[row][col] == '1')
                    {
                        spriteBatch.Draw(pixel, new Rectangle((int)position.X + x + col * scale, (int)position.Y + y + row * scale, scale, scale), color);
                    }
                }
            }

            x += (Width + 1) * scale;
        }
    }
